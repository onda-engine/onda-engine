/** Look up a manifest entry by slug or PascalCase name. */
export function manifestEntry(key: string): ManifestEntry | undefined {
  return MANIFEST.find((e) => e.slug === key || e.name === key)
}

/** Only the first-class components — the agent's safe default palette. */
export function firstClassEntries(): ManifestEntry[] {
  return MANIFEST.filter((e) => e.fidelity === 'first_class')
}

/** A compact, grouped text catalog for the agent prompt: one bullet per
 *  component with its fidelity, when-to-use, and its prop roles. */
export function compactCatalog(opts: { firstClassOnly?: boolean } = {}): string {
  const entries = opts.firstClassOnly ? firstClassEntries() : MANIFEST
  const byCat = new Map<string, ManifestEntry[]>()
  for (const e of entries) {
    const list = byCat.get(e.category) ?? []
    list.push(e)
    byCat.set(e.category, list)
  }
  const out: string[] = []
  for (const [cat, list] of [...byCat.entries()].sort()) {
    out.push(`## ${cat}`)
    for (const e of list.sort((a, b) => a.name.localeCompare(b.name))) {
      const tag = e.fidelity === 'first_class' ? '' : ` [${e.fidelity}]`
      const props = e.props.map((p) => `${p.name}${p.required ? '*' : ''}:${p.role}`).join(', ')
      out.push(
        `- **${e.name}**${tag} — ${e.description}${e.pickWhen ? ` (use: ${e.pickWhen})` : ''}`,
      )
      if (props) out.push(`  props: ${props}`)
    }
  }
  return out.join('\n')
}
